public class Main {
    public static void main(String[] args) {
        System.out.println("Hello Joseph I am in Java");
        System.out.println(true);

        //type of variables
        // String, int, float, boolean, char

        String p = "Joseph";
        System.out.println(p);

        int o = 85;
        System.out.println(o);

        float i = 8.3f;
        System.out.println(i);

        //you can use final keyword if you don't want to modify the variable or nobody does.
        final boolean y = true;
        System.out.println(y);

        char t = '#';
        t = 'y';
        System.out.println(t);

        String h = "Joseph white";
        System.out.println("Hello" + " " + h);


    }
}

